"""Auto-discovered skill evals from sibling test_cases.yaml files.

The eval framework discovers skills by scanning subdirectories of evals/skills/.
Each skill directory must contain:
  - system_prompt.md  — the system prompt sent to the agent
  - test_cases.yaml   — one or more test cases (query, schema, expected)

Optionally:
  - verify.py         — custom verification functions for tool-execution tests

Test cases support two verification modes:

  1. Static matching: `expected` contains field: value pairs. The framework
     asserts each field in the agent's response matches exactly. Good for
     knowledge-retrieval skills (e.g., kubernetes-docs, openshift-docs).

  2. Custom _fn: `expected: { _fn: function_name }` loads the named function
     from verify.py and calls it with (result, eval_workspace, provider_name).
     Good for skills where verification needs runtime data — e.g., tokens
     generated by tool execution, or values queried from a live OpenShift
     cluster that can't be hardcoded as static expected values.

Adding a new skill eval:
  1. Create the skill as a top-level directory (e.g., my-skill/) with SKILL.md
  2. Create evals/skills/my-skill/ with system_prompt.md and test_cases.yaml
  3. For static tests: set `expected` with known field values
  4. For dynamic tests: add verify.py with a function, reference it via _fn
"""

from __future__ import annotations

import importlib.util
import json
from collections.abc import Callable
from pathlib import Path
from typing import Any

import jsonschema
import pytest
import yaml

from ..framework.runner import RunResult

SKILLS_DIR = Path(__file__).parent


def _load_verify_fn(skill_dir: Path, fn_name: str) -> Callable[..., None]:
    """Load a verification function from a skill's verify.py.

    Called when a test case uses `expected: { _fn: <name> }`. The function
    is imported from verify.py in the skill's eval directory.
    """
    verify_path = skill_dir / "verify.py"
    if not verify_path.exists():
        raise FileNotFoundError(f"No verify.py in {skill_dir} for _fn: {fn_name}")
    spec = importlib.util.spec_from_file_location(f"verify_{skill_dir.name}", verify_path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    fn = getattr(mod, fn_name, None)
    if fn is None:
        raise AttributeError(f"verify.py in {skill_dir} has no function '{fn_name}'")
    return fn


def _discover_test_cases() -> list[pytest.param]:
    """Scan skill directories and build parametrized test cases."""
    params = []
    for skill_dir in sorted(SKILLS_DIR.iterdir()):
        if not skill_dir.is_dir():
            continue

        prompt_path = skill_dir / "system_prompt.md"
        cases_path = skill_dir / "test_cases.yaml"

        if not all(p.exists() for p in (prompt_path, cases_path)):
            continue

        prompt = prompt_path.read_text().strip()
        cases = yaml.safe_load(cases_path.read_text())

        for case in cases:
            expected = case["expected"]

            # If expected uses _fn, load the verify function from verify.py
            verify_fn: Callable[..., None] | None = None
            if isinstance(expected, dict) and "_fn" in expected:
                verify_fn = _load_verify_fn(skill_dir, expected["_fn"])

            params.append(
                pytest.param(
                    skill_dir.name,
                    case["name"],
                    case["query"],
                    case["schema"],
                    expected,
                    prompt,
                    verify_fn,
                    id=f"{skill_dir.name}-{case['name']}",
                )
            )

    return params


@pytest.mark.eval
@pytest.mark.asyncio
@pytest.mark.parametrize(
    "skill_name,case_name,query,output_schema,expected,system_prompt,verify_fn",
    _discover_test_cases(),
)
async def test_skill(
    provider_name: str,
    eval_runner: Callable[..., RunResult],
    eval_workspace: Path,
    skill_name: str,
    case_name: str,
    query: str,
    output_schema: dict,
    expected: dict[str, Any],
    system_prompt: str,
    verify_fn: Callable[..., None] | None,
) -> None:
    result = await eval_runner(
        query=query,
        system_prompt=system_prompt,
        output_schema=output_schema,
    )

    assert result.error is None, (
        f"{provider_name}/{skill_name}/{case_name} errored: {result.error}"
    )

    jsonschema.validate(result.raw, output_schema)

    # Custom verification — delegates to the skill's verify.py function
    if verify_fn is not None:
        verify_fn(result.raw, eval_workspace, provider_name)
        return

    # Static field matching — asserts each expected field matches exactly
    for field, expected_value in expected.items():
        actual = result.raw.get(field)
        assert actual is not None, (
            f"{case_name}: field '{field}' missing from response. "
            f"Got: {json.dumps(result.raw, indent=2)[:500]}"
        )
        if isinstance(expected_value, list):
            assert sorted(actual) == sorted(expected_value), (
                f"{case_name}: field '{field}' expected {expected_value}, got {actual}"
            )
        else:
            assert actual == expected_value, (
                f"{case_name}: field '{field}' expected {expected_value}, got {actual}"
            )
