def remove_timestamps(log_line: str, config: Optional[PipelineConfig] = None) -> str:
    """
    Remove timestamp entries from a single log line.
    
    Uses patterns from config.preprocessing.timestamp_patterns if provided,
    otherwise uses default hardcoded patterns.
    """
    if not log_line.strip():
        return log_line
    
    config = config or get_config()
    
    # Check if timestamp removal is enabled
    if not config.preprocessing.remove_timestamps:
        return log_line
    
    line = log_line
    
    # Use configurable patterns
    patterns = config.preprocessing.timestamp_patterns
    
    # Apply each pattern
    for i, pattern in enumerate(patterns):
        # Pattern at index 2 is the I/W/E + MMDD pattern, needs special replacement
        if i == 2 and pattern.startswith('^([IWE])'):
            line = re.sub(pattern, r'\1 ', line)
        # Pattern at index 3 is systemd/journal, needs case-insensitive
        elif i == 3 and 'Jan|Feb|Mar' in pattern:
            line = re.sub(pattern, '', line, flags=re.IGNORECASE)
        else:
            line = re.sub(pattern, '', line)
    
    # Clean up whitespace
    line = re.sub(r'\s+', ' ', line).strip()
    return line


