# LOG LEVEL PARSER
# =============================================================================
def parse_log_level(log_line: str) -> Optional[str]:
    """
    Parse log level from a log line.
    
    Prioritizes content-based detection (error/warning phrases)
    over structural prefixes (I/W/E) because Info-level logs often contain errors.
    
    Returns:
        'E' for Error, 'W' for Warning, 'I' for Info, None for Unknown
    """
    if not log_line.strip():
        return None
    s = log_line.strip()
    upper = s.upper()
    
    # 1) Content-based detection: Error phrases (highest priority)
    error_phrases = [
        r'\bFAILURE\b', r'\bFAILED\b', r'\bstatus=1/', r'\bstatus=2/', r'\bstatus=3/',
        r"exit-code", r"exit code", r"exited with", r"exited, code=",
        r"failed to start", r"could not start", r"failed with result",
        r'\bERROR\b', r'\bFATAL\b', r'\bCRASH', r'\bOOMKILLED\b',
        r'\bTIMEOUT\b', r'\bUNREACHABLE\b', r'\bREFUSED\b',
        r'connection refused', r'no such file', r'permission denied',
    ]
    for pat in error_phrases:
        if re.search(pat, upper):
            return 'E'
    
    # 2) Content-based detection: Warning phrases
    warn_phrases = [r'\bWARN(?:ING)?\b', r'\bDEPRECATED\b', r'\bRETRY\b', r'\bBACKOFF\b']
    for pat in warn_phrases:
        if re.search(pat, upper):
            return 'W'
    
    # 3) Structural prefix: I/W/E at start of line (Kubernetes-style logs)
    #    Handles both original (E0112, W0112, I0112) and preprocessed (E , W , I ) formats
    #    After timestamp removal, "W0112 17:02:33..." becomes "W ..."
    match = re.match(r'([IWEiwe])(?:\d{4}\b|\s)', s)
    if match:
        return match.group(1).upper()
    
    # 4) Bracketed level [INFO], [WARN], [ERROR]
    match = re.search(r'\[(INFO|WARN|WARNING|ERROR|ERR)\]', upper)
    if match:
        level_str = match.group(1)
        if level_str in ['INFO', 'INFORMATION']:
            return 'I'
        elif level_str in ['WARN', 'WARNING']:
            return 'W'
        elif level_str in ['ERROR', 'ERR']:
            return 'E'
    
    # 5) Info keywords
    if re.search(r'\bINFO\b', upper) or re.search(r'\bStarted\b', s):
        return 'I'
    
    return None


