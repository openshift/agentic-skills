# TIMESTAMP EXTRACTION FROM LOG LINES (for sampling by time)
# =============================================================================

# Month abbreviation -> number mapping for systemd/journal timestamps
_MONTH_MAP = {
    'Jan': 1, 'Feb': 2, 'Mar': 3, 'Apr': 4, 'May': 5, 'Jun': 6,
    'Jul': 7, 'Aug': 8, 'Sep': 9, 'Oct': 10, 'Nov': 11, 'Dec': 12,
}

# Pattern: Jan 12 14:22:56 (systemd/journal logs)
_TS_SYSTEMD = re.compile(
    r'^(Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\s+(\d{1,2})\s+(\d{2}):(\d{2}):(\d{2})(?:\.(\d+))?'
)

# Pattern: I0112 14:22:56 or E0112 14:22:56 (Kubernetes logs)
# Format is: <level><MMDD> <HH:MM:SS>  — e.g. I0112 means Info, Jan 12
_TS_KUBE = re.compile(
    r'^[IWEU](\d{4})\s+(\d{2}):(\d{2}):(\d{2})(?:\.(\d+))?'
)

# Pattern: 2024-01-12T14:22:56 (ISO timestamps)
_TS_ISO = re.compile(
    r'(\d{4})-(\d{2})-(\d{2})[T\s](\d{2}):(\d{2}):(\d{2})(?:\.(\d+))?'
)


def extract_timestamp_from_log(log_line: str) -> Optional[str]:
    """
    Extract a sortable timestamp string from a log line.

    Supported formats (checked in order):
        1. ``Jan 12 14:22:56``  – systemd / journal logs
        2. ``I0112 14:22:56``   – Kubernetes I/W/E/U prefixed logs
        3. ``2024-01-12T14:22:56`` – ISO 8601 timestamps

    Returns:
        A sortable string in the form ``MMDD-HH:MM:SS.ffffff`` (for formats 1 & 2)
        or ``YYYY-MM-DDTHH:MM:SS.ffffff`` (for ISO), or ``None`` if no timestamp
        could be parsed.  The returned string is designed for lexicographic
        ordering so that ``sorted()`` produces chronological order.
    """
    if not log_line:
        return None

    # 1. Systemd / journal:  Jan 12 14:22:56.123456
    m = _TS_SYSTEMD.match(log_line)
    if m:
        month = _MONTH_MAP.get(m.group(1), 0)
        day = int(m.group(2))
        frac = (m.group(6) or '0').ljust(6, '0')[:6]
        return f"{month:02d}{day:02d}-{m.group(3)}:{m.group(4)}:{m.group(5)}.{frac}"

    # 2. Kubernetes:  E0112 14:22:56.789
    m = _TS_KUBE.match(log_line)
    if m:
        mmdd = m.group(1)  # "MMDD" — 4 digits captured after the level char (e.g. "0112" = Jan 12)
        frac = (m.group(5) or '0').ljust(6, '0')[:6]
        return f"{mmdd}-{m.group(2)}:{m.group(3)}:{m.group(4)}.{frac}"

    # 3. ISO 8601:  2024-01-12T14:22:56.123Z
    m = _TS_ISO.search(log_line)
    if m:
        frac = (m.group(7) or '0').ljust(6, '0')[:6]
        return (f"{m.group(1)}-{m.group(2)}-{m.group(3)}T"
                f"{m.group(4)}:{m.group(5)}:{m.group(6)}.{frac}")

    return None


def sample_lines_by_timestamp(
    lines: List[str],
    first_n: int = 3,
    middle_n: int = 1,
    last_n: int = 3,
) -> List[str]:
    """
    Sample representative lines from a list using timestamp-based ordering.

    For a template cluster with many lines this returns a small, representative
    subset that shows *when the error started* (first N), a *mid-point*
    occurrence (middle N), and the *most recent* occurrences (last N).

    Algorithm:
        1. Try to parse a timestamp from each line via
           :func:`extract_timestamp_from_log`.
        2. If **any** timestamps were found, sort lines by timestamp.
           Otherwise, keep original order (log files are usually
           chronologically ordered).
        3. Sample ``first_n + middle_n + last_n`` lines (default 7).
        4. If the cluster has fewer lines than the sample size, return all
           lines (no sampling needed).

    Args:
        lines:    All original log lines belonging to one template cluster.
        first_n:  Number of earliest lines to include  (default 3).
        middle_n: Number of middle lines to include     (default 1).
        last_n:   Number of latest lines to include     (default 3).

    Returns:
        A list of sampled original log lines (order preserved from the
        sorted/original sequence).
    """
    total_sample = first_n + middle_n + last_n  # default 7

    if len(lines) <= total_sample:
        return list(lines)  # nothing to sample — return all

    # Attempt timestamp extraction
    ts_line_pairs = []
    has_timestamps = False
    for line in lines:
        ts = extract_timestamp_from_log(line)
        ts_line_pairs.append((ts, line))
        if ts is not None:
            has_timestamps = True

    # Sort by timestamp if available; otherwise keep original order
    if has_timestamps:
        # Lines without a timestamp get a sentinel that sorts first
        ts_line_pairs.sort(key=lambda pair: pair[0] if pair[0] is not None else '')

    sorted_lines = [line for _, line in ts_line_pairs]

    # Sample indices
    n = len(sorted_lines)
    first_indices = list(range(0, min(first_n, n)))
    last_indices = list(range(max(n - last_n, 0), n))
    mid_start = (n // 2) - (middle_n // 2)
    middle_indices = list(range(mid_start, min(mid_start + middle_n, n)))

    # Merge indices, deduplicate, preserve order
    seen = set()
    sampled = []
    for idx in first_indices + middle_indices + last_indices:
        if idx not in seen:
            seen.add(idx)
            sampled.append(sorted_lines[idx])

    return sampled


