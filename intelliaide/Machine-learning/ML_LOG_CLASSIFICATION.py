def save_templates_to_file(
    template_list: List[Dict],
    cluster_sizes: Dict,
    logs: List[Tuple[str, str]],
    event_ids: List,
    output_dir: str,
    level_suffix: str,
    filename_prefix: Optional[str] = None,
    cluster_to_lines: Optional[Dict[int, List[str]]] = None,
    max_original_lines_per_cluster: int = 5000,
) -> str:
    """
    Save the log lines for one level to a file (no templates/headers).
    Also saves a companion template file listing each Drain3 template
    with its cluster size, named ``<filename_prefix>_<level_suffix>_templates.txt``.
    
    Args:
        template_list: List of template dicts
        cluster_sizes: Dict of cluster_id -> size
        logs: List of (file_path, line) tuples
        event_ids: Event IDs from Drain3
        output_dir: Output directory
        level_suffix: Level name (e.g., 'Warning', 'Information')
        filename_prefix: Optional filename prefix
        cluster_to_lines: Mapping of cluster_id -> original lines
        max_original_lines_per_cluster: Ignored
    
    Returns:
        Path to saved log-lines file
    """
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    if filename_prefix:
        filename = output_path / f"{filename_prefix}_{level_suffix}.txt"
        template_filename = output_path / f"{filename_prefix}_{level_suffix}_templates.txt"
    else:
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        filename = output_path / f"log_classification_{level_suffix}_{timestamp}.txt"
        template_filename = output_path / f"log_classification_{level_suffix}_templates_{timestamp}.txt"
    
    cluster_to_lines = cluster_to_lines or {}
    
    # Collect all log lines from all clusters
    all_lines = []
    for tmpl in template_list:
        cid = tmpl['id']
        lines = cluster_to_lines.get(cid, [])
        all_lines.extend(lines)
    
    # Write only the log lines
    with open(filename, 'w', encoding='utf-8') as f:
        for line in all_lines:
            f.write(line + "\n")

    # ── Save companion template file ──
    if template_list:
        with open(template_filename, 'w', encoding='utf-8') as f:
            f.write(f"# {level_suffix} Templates ({len(template_list)} templates)\n")
            f.write(f"# Each template shown with cluster size (number of matching log lines)\n")
            f.write("=" * 80 + "\n\n")
            for i, tmpl in enumerate(template_list, 1):
                cid = tmpl['id']
                size = cluster_sizes.get(cid, tmpl.get('size', len(cluster_to_lines.get(cid, []))))
                template_text = tmpl.get('template', '(unknown)')
                f.write(f"Template {i}  |  cluster_id={cid}  |  lines={size}\n")
                f.write(f"  {template_text}\n\n")
    
    return str(filename)


