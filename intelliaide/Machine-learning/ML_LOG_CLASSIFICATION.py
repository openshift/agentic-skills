def main():
    """
    Main function demonstrating the pipeline.
    
    Usage:
        python testing-log.py                             # Use default test file with analyze_logs
        python testing-log.py file.log                    # Process single file with analyze_logs
        python testing-log.py file.log --analyze          # Use analyze_logs (default, explicit)
        python testing-log.py file.log --legacy           # Use legacy process_log_file
        python testing-log.py file.log --compare          # Compare all branches
        python testing-log.py file.log --config path      # Use specific config file
        python testing-log.py file.log --output dir       # Specify output directory
        python testing-log.py file.log --chunk            # Force chunk pipeline (legacy mode)
        python testing-log.py file.log --entire           # Force entire file pipeline (legacy mode)
        python testing-log.py file.log --sequential       # Force sequential chunk processing (legacy mode)
    """
    import sys
    import json as json_module
    
    script_dir = Path(__file__).parent
    
    # Parse arguments
    args = sys.argv[1:]
    
    # Parse flags
    analyze_mode = '--analyze' in args or '--legacy' not in args  # Default to analyze mode
    legacy_mode = '--legacy' in args
    compare_mode = '--compare' in args
    force_chunk = '--chunk' in args
    force_entire = '--entire' in args
    force_sequential = '--sequential' in args
    
    # Parse --config and --output options
    config_path = None
    output_dir = None
    
    filtered_args = []
    i = 0
    while i < len(args):
        if args[i] == '--config' and i + 1 < len(args):
            config_path = args[i + 1]
            i += 2
        elif args[i] == '--output' and i + 1 < len(args):
            output_dir = args[i + 1]
            i += 2
        elif args[i] not in ['--compare', '--chunk', '--entire', '--sequential', '--analyze', '--legacy']:
            filtered_args.append(args[i])
            i += 1
        else:
            i += 1
    
    args = filtered_args
    
    # Get file path
    if args:
        file_path = str(Path(args[0]).resolve())
    else:
        # Default test files
        default_files = [
            script_dir / "current.log",
            script_dir / 'crio_service.log',
            script_dir / "kubelet_service.log",
        ]
        for f in default_files:
            if f.exists():
                file_path = str(f)
                break
        else:
            print("Error: No log file found.")
            print("\nUsage: python testing-log.py <file.log> [OPTIONS]")
            print("\nOptions:")
            print("  --analyze          Use analyze_logs function (default)")
            print("  --legacy           Use legacy process_log_file function")
            print("  --config <path>    Use specific config file")
            print("  --output <dir>     Specify output directory")
            print("  --compare          Compare all branches (legacy mode)")
            print("  --chunk            Force chunk pipeline (legacy mode)")
            print("  --entire           Force entire file pipeline (legacy mode)")
            print("  --sequential       Force sequential chunk processing (legacy mode)")
            sys.exit(1)
    
    # Validate file exists
    if not Path(file_path).exists():
        print(f"Error: File not found: {file_path}")
        sys.exit(1)
    
    # Check drain3
    if not DRAIN3_AVAILABLE:
        print("Error: drain3 is not installed. Install with: pip install drain3")
        sys.exit(1)
    
    # =========================================================================
    # MODE: analyze_logs (NEW - default)
    # =========================================================================
    if analyze_mode and not legacy_mode and not compare_mode:
        print("\n" + "=" * 60)
        print("ANALYZE LOGS MODE (Agent Interface)")
        print("=" * 60)
        print(f"File: {file_path}")
        print(f"Config: {config_path or 'auto-detect'}")
        print(f"Output: {output_dir or 'auto'}")
        print("=" * 60 + "\n")
        
        # Call analyze_logs
        result = analyze_logs(
            file_path=file_path,
            output_dir=output_dir,
            config_path=config_path,
        )
        
        # Print JSON result
        print("\n" + "=" * 60)
        print("JSON RESPONSE")
        print("=" * 60)
        print(json_module.dumps(result, indent=2))
        print("=" * 60)
        
        if result['status'] == 'error':
            sys.exit(1)
    
    # =========================================================================
    # MODE: Legacy (process_log_file)
    # =========================================================================
    else:
        # Load config (will auto-detect log_config.json if config_path not specified)
        config = load_config(config_path)
        
        # Apply command-line overrides
        overrides = {}
        if output_dir:
            overrides['output_dir'] = output_dir
        if force_chunk:
            overrides['use_chunk_pipeline'] = True
        if force_entire:
            overrides['use_chunk_pipeline'] = False
        if force_sequential:
            overrides['parallel_chunks'] = False
        
        if compare_mode:
            # Compare all branches
            compare_branches(file_path)
        else:
            # Process with config
            result = process_log_file(file_path, config=config, **overrides)
            if result.get('error'):
                print(f"Error: {result['error']}")
                sys.exit(1)


if __name__ == "__main__":
    main()