def create_drain3_miner(config: Optional[PipelineConfig] = None) -> 'TemplateMiner':
    """Create and configure a new Drain3 TemplateMiner instance."""
    config = config or get_config()
    drain_config = TemplateMinerConfig()
    drain_config.sim_th = config.drain3.similarity_threshold
    drain_config.depth = config.drain3.depth
    drain_config.max_children = config.drain3.max_children
    drain_config.profiling_enabled = False
    return TemplateMiner(config=drain_config)


def extract_drain3_results(miner: 'TemplateMiner') -> Tuple[Dict, List[Dict]]:
    """Extract cluster sizes and template list from a Drain3 miner."""
    cluster_sizes = {}
    template_list = []
    
    clusters = miner.drain.clusters if hasattr(miner, 'drain') else []
    
    # Handle both list and dict formats
    if isinstance(clusters, dict):
        cluster_list = list(clusters.values())
    elif hasattr(clusters, '__iter__'):
        cluster_list = list(clusters)
    else:
        cluster_list = []
    
    for cluster in cluster_list:
        try:
            # Use the ACTUAL cluster_id from the LogCluster object
            # This ensures consistency with miner.match() results
            cid = getattr(cluster, 'cluster_id', None)
            if cid is None:
                continue
            size = getattr(cluster, 'size', 1)
            template = cluster.get_template() if hasattr(cluster, 'get_template') else str(cluster)
            cluster_sizes[cid] = size
            template_list.append({'id': cid, 'template': template, 'size': size})
        except Exception:
            pass
    
    return cluster_sizes, template_list


def process_lines_with_drain3(
    lines: List[str],
    config: Optional[PipelineConfig] = None
) -> Tuple[Dict, List[Dict], List]:
    """Process lines through Drain3 in batches.
    
    Returns:
        Tuple of (cluster_sizes, template_list, event_ids)
        - event_ids: List of FINAL cluster_ids for each input line
    """
    if not DRAIN3_AVAILABLE or not lines:
        return {}, [], [None] * len(lines)
    
    config = config or get_config()
    batch_size = config.drain3.batch_size
    
    miner = create_drain3_miner(config)
    
    # STEP 1: Process all lines to build the clusters
    for i in range(0, len(lines), batch_size):
        batch = lines[i:i + batch_size]
        for line in batch:
            if line.strip():
                miner.add_log_message(line)
    
    # STEP 2: Extract final cluster results
    cluster_sizes, template_list = extract_drain3_results(miner)
    
    # STEP 3: Re-match each line to its FINAL cluster ID
    # This ensures event_ids reflect the final cluster assignments
    event_ids = []
    for line in lines:
        if line.strip():
            # match() returns the cluster without modifying the miner
            result = miner.match(line)
            if result:
                event_ids.append(result.cluster_id)
            else:
                event_ids.append(None)
        else:
            event_ids.append(None)
    
    return cluster_sizes, template_list, event_ids




