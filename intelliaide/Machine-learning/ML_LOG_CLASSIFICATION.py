# BRANCH 1: ENTIRE FILE PIPELINE
# =============================================================================
def entire_file_pipeline(file_path: str, config: Optional[PipelineConfig] = None) -> Dict[str, Dict[str, List[str]]]:
    """
    BRANCH 1: Process entire file at once.
    
    Pipeline:
        1. Read entire file into memory
        2. Process all lines through timestamp parser
        3. Categorize all lines by log level
    
    Best for: Smaller files (< file_size_threshold_mb)
    
    Args:
        file_path: Path to the log file
        config: Pipeline configuration (uses global config if None)
    
    Returns:
        Dict mapping level ('E', 'W', 'I', 'UNKNOWN') to
        {"preprocessed": [lines], "original": [lines]}
    """
    config = config or get_config()
    
    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            log_content = f.read()
    except Exception as e:
        if config.verbose:
            print(f"[EntireFile] Error reading file {file_path}: {e}")
        return {}
    
    if not log_content.strip():
        return {}
    
    result = defaultdict(lambda: {"preprocessed": [], "original": []})
    for line in log_content.split('\n'):
        if not line.strip():
            continue
        # Step 1: Remove timestamps (using config)
        preprocessed_line = remove_timestamps(line, config)
        # Step 2: Categorize by log level (using config)
        level = parse_log_level(preprocessed_line)
        if level == 'E':
            result['E']["preprocessed"].append(preprocessed_line)
            result['E']["original"].append(line)
        elif level == 'W':
            result['W']["preprocessed"].append(preprocessed_line)
            result['W']["original"].append(line)
        elif level == 'I':
            result['I']["preprocessed"].append(preprocessed_line)
            result['I']["original"].append(line)
        else:
            result['UNKNOWN']["preprocessed"].append(preprocessed_line)
            result['UNKNOWN']["original"].append(line)
    
    return dict(result)


# =============================================================================
# BRANCH 2: CHUNK PIPELINE - Core Functions
# =============================================================================
def chunk_file_reader(file_path: str, chunk_size: int) -> Generator[List[str], None, None]:
    """
    Generator that reads a file in chunks.
    
    Yields:
        List[str]: A chunk of lines (up to chunk_size lines per chunk)
    """
    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            chunk = []
            for line in f:
                stripped = line.rstrip('\n\r')
                if stripped:
                    chunk.append(stripped)
                    if len(chunk) >= chunk_size:
                        yield chunk
                        chunk = []
            if chunk:
                yield chunk
    except Exception as e:
        print(f"[ChunkReader] Error reading file {file_path}: {e}")


def process_single_chunk(chunk: List[str], config: Optional[PipelineConfig] = None) -> Dict[str, Dict[str, List[str]]]:
    """
    Process a single chunk: timestamp removal + categorization.
    
    This function is the worker for both sequential and parallel processing.
    
    Args:
        chunk: List of raw log lines
        config: Pipeline configuration (uses global config if None)
    
    Returns:
        Dict mapping level ('E', 'W', 'I', 'UNKNOWN') to
        {"preprocessed": [lines], "original": [lines]}
    """
    config = config or get_config()
    categorized = defaultdict(lambda: {"preprocessed": [], "original": []})
    
    for line in chunk:
        # Step 1: Remove timestamps (using config)
        preprocessed = remove_timestamps(line, config)
        # Step 2: Categorize by log level (using config)
        level = parse_log_level(preprocessed)
        if level == 'E':
            categorized['E']["preprocessed"].append(preprocessed)
            categorized['E']["original"].append(line)
        elif level == 'W':
            categorized['W']["preprocessed"].append(preprocessed)
            categorized['W']["original"].append(line)
        elif level == 'I':
            categorized['I']["preprocessed"].append(preprocessed)
            categorized['I']["original"].append(line)
        else:
            categorized['UNKNOWN']["preprocessed"].append(preprocessed)
            categorized['UNKNOWN']["original"].append(line)
    
    return dict(categorized)


def merge_chunk_results(results: List[Dict[str, Dict[str, List[str]]]]) -> Dict[str, Dict[str, List[str]]]:
    """Merge categorized results from multiple chunks."""
    merged = defaultdict(lambda: {"preprocessed": [], "original": []})
    for result in results:
        for level, data in result.items():
            if isinstance(data, dict) and "preprocessed" in data:
                merged[level]["preprocessed"].extend(data["preprocessed"])
                merged[level]["original"].extend(data["original"])
            elif isinstance(data, list):
                # Backward compat: old format without originals
                merged[level]["preprocessed"].extend(data)
                merged[level]["original"].extend(data)
    return dict(merged)


# =============================================================================
# BRANCH 2A: CHUNK PIPELINE - SEQUENTIAL
# =============================================================================
def chunk_pipeline_sequential(file_path: str, config: Optional[PipelineConfig] = None) -> Dict[str, List[str]]:
    """
    BRANCH 2A: Process file in chunks SEQUENTIALLY.
    
    Pipeline:
        1. Read file chunk by chunk
        2. For each chunk:
           - Remove timestamps
           - Categorize by log level
        3. Accumulate results
    
    Args:
        file_path: Path to the log file
        config: Pipeline configuration (uses global config if None)
    
    Returns:
        Dict mapping level ('E', 'W', 'I', 'UNKNOWN') to list of preprocessed lines
    """
    config = config or get_config()
    by_level = defaultdict(list)
    chunk_count = 0
    
    for chunk in chunk_file_reader(file_path, config.chunk_size):
        chunk_count += 1
        # Process this chunk (passing config)
        chunk_result = process_single_chunk(chunk, config)
        # Accumulate results
        for level, lines in chunk_result.items():
            by_level[level].extend(lines)
    
    return dict(by_level)


# =============================================================================
# BRANCH 2B: CHUNK PIPELINE - PARALLEL
# =============================================================================
def chunk_pipeline_parallel(file_path: str, config: Optional[PipelineConfig] = None) -> Dict[str, List[str]]:
    """
    BRANCH 2B: Process file chunks IN PARALLEL using multiprocessing.
    
    Pipeline:
        1. Read all chunks from file (sequential I/O)
        2. Process chunks in parallel (timestamp + categorization)
        3. Merge results from all chunks
    
    Note: For parallel processing, config is loaded fresh in each worker process.
    
    Args:
        file_path: Path to the log file
        config: Pipeline configuration (uses global config if None)
    
    Returns:
        Dict mapping level ('E', 'W', 'I', 'UNKNOWN') to list of preprocessed lines
    """
    config = config or get_config()
    
    # Step 1: Read all chunks into memory (I/O is sequential)
    chunks = list(chunk_file_reader(file_path, config.chunk_size))
    
    if not chunks:
        return {}
    
    num_chunks = len(chunks)
    
    # Check if parallel is worth it
    if num_chunks < config.min_chunks_for_parallel:
        # Fall back to sequential for small number of chunks
        return chunk_pipeline_sequential(file_path, config)
    
    # Determine worker count
    max_workers = config.chunk_workers
    if max_workers is None:
        max_workers = min(multiprocessing.cpu_count(), num_chunks, 8)
    
    # Step 2: Process chunks in parallel
    chunk_results = []
    
    with ProcessPoolExecutor(max_workers=max_workers) as executor:
        futures = [executor.submit(process_single_chunk, chunk) for chunk in chunks]
        
        # Collect results (preserves order)
        for future in futures:
            try:
                result = future.result()
                chunk_results.append(result)
            except Exception as e:
                print(f"[ChunkParallel] Error processing chunk: {e}")
    
    # Step 3: Merge all chunk results
    return merge_chunk_results(chunk_results)


# =============================================================================
# UNIFIED CHUNK PIPELINE (auto-selects sequential or parallel)
# =============================================================================
def chunk_pipeline(file_path: str, config: Optional[PipelineConfig] = None) -> Dict[str, List[str]]:
    """
    BRANCH 2: Process file in chunks.
    
    Automatically selects SEQUENTIAL or PARALLEL mode based on config.parallel_chunks.
    
    Args:
        file_path: Path to the log file
        config: Pipeline configuration (uses global config if None)
    
    Returns:
        Dict mapping level ('E', 'W', 'I', 'UNKNOWN') to list of preprocessed lines
    """
    config = config or get_config()
    if config.parallel_chunks:
        return chunk_pipeline_parallel(file_path, config)
    else:
        return chunk_pipeline_sequential(file_path, config)


def build_cluster_to_lines(
    lines: List[str],
    event_ids: List,
) -> Dict[int, List[str]]:
    """Map cluster_id -> list of original log lines that belong to that cluster."""
    cluster_to_lines: Dict[int, List[str]] = defaultdict(list)
    for i, line in enumerate(lines):
        if i >= len(event_ids):
            break
        cid = event_ids[i]
        if cid is not None:
            cluster_to_lines[cid].append(line)
    return dict(cluster_to_lines)


