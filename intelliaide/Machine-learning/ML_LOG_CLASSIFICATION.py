def process_log_file(
    file_path: str,
    config: Optional[PipelineConfig] = None,
    config_path: Optional[str] = None,
    **overrides
) -> Dict:
    """
    Main function to process a single log file.
    
    Configuration is loaded in the following priority:
        1. Explicit config object passed as parameter
        2. Config loaded from config_path JSON file
        3. Config loaded from log_config.json (auto-detected)
        4. Default configuration
    
    Override parameters (passed as **kwargs) can override config values:
        - output_dir: Output directory for template files
        - use_chunk_pipeline: Force branch selection (None=auto, True=chunk, False=entire)
        - parallel_chunks: For CHUNK pipeline: True = parallel, False = sequential
        - chunk_size: Lines per chunk
        - chunk_workers: Workers for parallel chunk processing
        - file_size_threshold_mb: Threshold for auto branch selection
    
    TWO BRANCHES:
        1. ENTIRE FILE: Loads entire file into memory, processes at once
        2. CHUNK: Reads file in chunks, processes each chunk
           - SEQUENTIAL: Chunks processed one at a time
           - PARALLEL: Chunks processed in parallel
    
    Returns:
        Dict with processing results:
            - file_path: Input file path
            - branch_used: 'ENTIRE_FILE' or 'CHUNK_SEQUENTIAL' or 'CHUNK_PARALLEL'
            - file_size_mb: File size in MB
            - elapsed_ms: Processing time
            - saved_files: Dict of level -> output file path
            - summary: Dict of level -> {count, templates_count, path}
            - config_used: Configuration file path or 'default'
            - error: Error message if failed
    """
    if not DRAIN3_AVAILABLE:
        return {
            "file_path": file_path,
            "error": "drain3 not installed. pip install drain3",
        }
    
    # Validate file exists
    if not Path(file_path).exists():
        return {
            "file_path": file_path,
            "error": f"File not found: {file_path}",
        }
    
    # Load configuration
    if config is not None:
        pass  # Use provided config
    elif config_path:
        config = load_config(config_path)
    else:
        config = get_config()
    
    # Apply overrides from kwargs
    if 'output_dir' in overrides and overrides['output_dir'] is not None:
        config.output_dir = overrides['output_dir']
    if 'use_chunk_pipeline' in overrides:
        config.use_chunk_pipeline = overrides['use_chunk_pipeline']
    if 'parallel_chunks' in overrides:
        config.parallel_chunks = overrides['parallel_chunks']
    if 'chunk_size' in overrides:
        config.chunk_size = overrides['chunk_size']
    if 'chunk_workers' in overrides:
        config.chunk_workers = overrides['chunk_workers']
    if 'file_size_threshold_mb' in overrides:
        config.file_size_threshold_mb = overrides['file_size_threshold_mb']
    
    # Set default output directory if not specified
    if config.output_dir is None:
        config.output_dir = str(Path(__file__).parent / "output")
    
    # Get file info
    try:
        file_size_mb = os.path.getsize(file_path) / (1024 * 1024)
    except:
        file_size_mb = 0
    
    # Determine which branch to use
    if config.use_chunk_pipeline is None:
        # Auto-select based on file size
        use_chunk = file_size_mb > config.file_size_threshold_mb
    else:
        use_chunk = config.use_chunk_pipeline
    
    # Determine branch name for reporting
    if use_chunk:
        branch_used = "CHUNK_PARALLEL" if config.parallel_chunks else "CHUNK_SEQUENTIAL"
    else:
        branch_used = "ENTIRE_FILE"
    
    level_names = {'E': 'Error', 'W': 'Warning', 'I': 'Information', 'UNKNOWN': 'Unknown'}
    stem = Path(file_path).stem or Path(file_path).name
    
    if config.print_progress:
        print(f"\n{'='*60}")
        print(f"Processing: {Path(file_path).name}")
        print(f"File size: {file_size_mb:.2f} MB")
        print(f"Branch: {branch_used}")
        if use_chunk:
            print(f"Chunk size: {config.chunk_size:,} lines")
            if config.parallel_chunks:
                workers = config.chunk_workers or f"auto (up to {min(multiprocessing.cpu_count(), 8)})"
                print(f"Chunk workers: {workers}")
        print(f"{'='*60}")
    
    # =========================================================================
    # STEP 1: Read and Categorize (based on branch)
    # =========================================================================
    start_time = time.perf_counter()
    
    try:
        if use_chunk:
            # BRANCH 2: CHUNK PIPELINE
            by_level = chunk_pipeline(file_path, config)
        else:
            # BRANCH 1: ENTIRE FILE PIPELINE
            by_level = entire_file_pipeline(file_path, config)
    except Exception as e:
        return {
            'file_path': file_path,
            'error': str(e),
            'branch_used': branch_used,
            'file_size_mb': file_size_mb,
        }
    
    categorize_time = time.perf_counter() - start_time
    
    if not by_level:
        return {
            'file_path': file_path,
            'error': None,
            'branch_used': branch_used,
            'file_size_mb': file_size_mb,
            'saved_files': {},
            'summary': {},
            'elapsed_ms': categorize_time * 1000,
        }
    
    # =========================================================================
    # STEP 2: Process with Drain3 (initial pass for all levels)
    # =========================================================================
    saved_files = {}
    summary = {}
    level_results = {}
    
    # First pass: Process all levels with Drain3
    for level in config.levels_to_process:
        level_data = by_level.get(level, {})
        # Support both new format {"preprocessed": [...], "original": [...]}
        # and old format (plain list)
        if isinstance(level_data, dict) and "preprocessed" in level_data:
            preprocessed_lines = level_data["preprocessed"]
            original_lines = level_data["original"]
        elif isinstance(level_data, list):
            preprocessed_lines = level_data
            original_lines = level_data  # fallback: no originals available
        else:
            continue
        if not preprocessed_lines:
            continue
        
        # Process with Drain3 using PREPROCESSED lines (timestamp-free, better clustering)
        cluster_sizes, template_list, event_ids = process_lines_with_drain3(
            preprocessed_lines, config
        )
        
        # Build cluster-to-lines mapping using ORIGINAL lines (with timestamps)
        # so output files contain the full original log lines
        cluster_to_lines = build_cluster_to_lines(original_lines, event_ids)
        
        level_results[level] = {
            'lines': original_lines,           # original lines for output
            'preprocessed_lines': preprocessed_lines,  # kept for internal use
            'template_list': template_list,
            'cluster_sizes': cluster_sizes,
            'event_ids': event_ids,
            'cluster_to_lines': cluster_to_lines,
        }
    
    # =========================================================================
    # STEP 3: Classify templates in W/I/U -> Error templates, Config templates, Regular
    #         (Working at TEMPLATE level - entire clusters move together)
    # =========================================================================
    classified_results = {}
    error_templates_to_merge = []  # Error templates found in W/I/U
    config_templates_to_merge = []  # Config templates found in W/I/U
    
    for level in ['W', 'I', 'UNKNOWN']:
        if level not in level_results:
            continue
        
        result = level_results[level]
        template_list = result['template_list']
        cluster_to_lines = result['cluster_to_lines']
        lines = result['lines']
        event_ids = result['event_ids']
        
        # Classify templates into error, config, regular
        classification = classify_templates_for_level(
            level, template_list, cluster_to_lines, lines, event_ids
        )
        
        classified_results[level] = classification
        
        # Collect error templates to merge into Error level
        if classification['error']['templates']:
            error_templates_to_merge.append({
                'source_level': level,
                **classification['error']
            })
            if config.print_progress:
                error_count = sum(len(v) for v in classification['error']['cluster_to_lines'].values())
                print(f"  [Shift->Error] {len(classification['error']['templates'])} templates ({error_count} lines) from {level_names.get(level, level)}")
        
        # Collect config templates
        if classification['config']['templates']:
            config_templates_to_merge.append({
                'source_level': level,
                **classification['config']
            })
            if config.print_progress:
                config_count = sum(len(v) for v in classification['config']['cluster_to_lines'].values())
                print(f"  [Shift->Config] {len(classification['config']['templates'])} templates ({config_count} lines) from {level_names.get(level, level)}")
        
        # Update level results with only regular templates
        regular = classification['regular']
        regular_lines, regular_event_ids = get_lines_for_clusters(
            lines, event_ids, regular['cluster_ids']
        )
        
        level_results[level] = {
            'lines': regular_lines,
            'template_list': regular['templates'],
            'cluster_sizes': {t['id']: t['size'] for t in regular['templates']},
            'event_ids': regular_event_ids,
            'cluster_to_lines': regular['cluster_to_lines'],
        }
    
    # =========================================================================
    # STEP 4: Merge error templates from W/I/U into Error level
    #         (Keep Drain3 templates - no re-processing needed)
    # =========================================================================
    if error_templates_to_merge:
        # Get existing Error level data
        existing_error = level_results.get('E', {
            'lines': [], 'template_list': [], 'cluster_sizes': {},
            'event_ids': [], 'cluster_to_lines': {}
        })
        
        # Merge error templates from W/I/U
        merged_error_templates = list(existing_error.get('template_list', []))
        merged_error_cluster_to_lines = dict(existing_error.get('cluster_to_lines', {}))
        merged_error_lines = list(existing_error.get('lines', []))
        merged_error_event_ids = list(existing_error.get('event_ids', []))
        
        for error_data in error_templates_to_merge:
            merged_error_templates.extend(error_data['templates'])
            merged_error_cluster_to_lines.update(error_data['cluster_to_lines'])
            # Get lines for these clusters from original data
            source_level = error_data['source_level']
            if source_level in classified_results:
                orig_lines = by_level.get(source_level, [])
                orig_result = level_results.get(source_level, {})
                # Add lines belonging to error clusters
                for cid, clines in error_data['cluster_to_lines'].items():
                    merged_error_lines.extend(clines)
                    merged_error_event_ids.extend([cid] * len(clines))
        
        level_results['E'] = {
            'lines': merged_error_lines,
            'template_list': merged_error_templates,
            'cluster_sizes': {t['id']: t['size'] for t in merged_error_templates},
            'event_ids': merged_error_event_ids,
            'cluster_to_lines': merged_error_cluster_to_lines,
        }
    
    # =========================================================================
    # STEP 5: Save templates for each level (filtered - without error/config templates)
    #         For Error level: apply Pareto/80-20 frequency classification
    # =========================================================================
    frequency_stats = {}  # Store frequency classification stats for return
    
    for level in config.levels_to_process:
        if level not in level_results:
            continue
        
        result = level_results[level]
        lines = result.get('lines', [])
        template_list = result.get('template_list', [])
        cluster_sizes = result.get('cluster_sizes', {})
        event_ids = result.get('event_ids', [])
        cluster_to_lines = result.get('cluster_to_lines', {})
        
        if not template_list:
            continue
        
        level_name = level_names.get(level, level)
        
        # Prepare logs tuple format for save_templates_to_file
        level_logs = [(file_path, line) for line in lines]
        
        # =====================================================================
        # SPECIAL HANDLING FOR ERROR LEVEL: Tiered Output (Pareto/80-20)
        # =====================================================================
        if level == 'E' and config.frequency.enable_tiered_error_output:
            # Classify error templates by frequency
            freq_classification = classify_templates_by_frequency(
                template_list, cluster_to_lines, config.frequency
            )
            
            high_freq = freq_classification['high_freq']
            rare = freq_classification['rare']
            
            # Store stats for return value
            frequency_stats['Error'] = {
                'high_freq': high_freq['stats'],
                'rare': rare['stats'],
                'threshold_pct': config.frequency.high_freq_cumulative_threshold * 100,
            }
            
            saved_error_paths = {}
            
            # Save HIGH-FREQUENCY templates (all lines included)
            if high_freq['templates']:
                high_freq_path = save_high_freq_summary(
                    high_freq['templates'],
                    high_freq['cluster_to_lines'],
                    config.output_dir,
                    level_name,
                    filename_prefix=stem,
                )
                saved_error_paths['high_freq'] = high_freq_path
                
                if config.print_progress:
                    pct = high_freq['stats']['line_coverage_pct']
                    print(f"  [highfreq_error] {high_freq['total_lines']:,} lines -> "
                          f"{len(high_freq['templates'])} templates ({pct:.1f}% coverage)")
            
            # Save RARE/PRIORITY templates (full detail)
            if rare['templates']:
                rare_logs = [(file_path, line) for cid in rare['cluster_ids'] 
                             for line in rare['cluster_to_lines'].get(cid, [])]
                rare_event_ids = [cid for cid in rare['cluster_ids'] 
                                  for _ in rare['cluster_to_lines'].get(cid, [])]
                
                rare_path = save_rare_priority_templates(
                    rare['templates'],
                    rare['cluster_to_lines'],
                    rare_logs,
                    rare_event_ids,
                    config.output_dir,
                    level_name,
                    filename_prefix=stem,
                )
                saved_error_paths['rare'] = rare_path
                
                if config.print_progress:
                    pct = rare['stats']['line_coverage_pct']
                    print(f"  [rare_error] {rare['total_lines']:,} lines -> "
                          f"{len(rare['templates'])} templates ({pct:.1f}% coverage)")
            
            # Also save combined Error file for backward compatibility
            saved_path = save_templates_to_file(
                template_list, cluster_sizes, level_logs, event_ids,
                config.output_dir, level_name, filename_prefix=stem,
                cluster_to_lines=cluster_to_lines,
            )
            saved_error_paths['combined'] = saved_path
            
            saved_files[level_name] = saved_error_paths
            summary[level_name] = {
                'count': len(lines),
                'templates_count': len(template_list),
                'paths': saved_error_paths,
                'frequency_classification': {
                    'high_freq_templates': len(high_freq['templates']),
                    'high_freq_lines': high_freq['total_lines'],
                    'rare_templates': len(rare['templates']),
                    'rare_lines': rare['total_lines'],
                    'threshold_pct': config.frequency.high_freq_cumulative_threshold * 100,
                },
            }
            
            if config.print_progress:
                print(f"  [Error] {len(lines):,} logs -> {len(template_list)} templates (combined)")
        
        else:
            # =====================================================================
            # STANDARD HANDLING FOR OTHER LEVELS (W, I, UNKNOWN)
            # =====================================================================
            # Save templates with all original lines
            saved_path = save_templates_to_file(
                template_list, cluster_sizes, level_logs, event_ids,
                config.output_dir, level_name, filename_prefix=stem,
                cluster_to_lines=cluster_to_lines,
            )
            
            saved_files[level_name] = saved_path
            summary[level_name] = {
                'count': len(lines),
                'templates_count': len(template_list),
                'path': saved_path,
            }
            
            if config.print_progress:
                print(f"  [{level_name}] {len(lines):,} logs -> {len(template_list)} templates")
    
    # =========================================================================
    # STEP 6: Save ConfigChanges templates (from W/I/U - keep Drain3 templates)
    # =========================================================================
    if config_templates_to_merge:
        # Merge all config templates (keeping Drain3 structure)
        merged_config = merge_template_results([
            {'templates': d['templates'], 'cluster_to_lines': d['cluster_to_lines'], 'cluster_ids': d['cluster_ids']}
            for d in config_templates_to_merge
        ])
        
        config_template_list = merged_config['templates']
        config_cluster_to_lines = merged_config['cluster_to_lines']
        
        # Get all config lines
        all_config_lines = []
        config_event_ids = []
        for cid, clines in config_cluster_to_lines.items():
            all_config_lines.extend(clines)
            config_event_ids.extend([cid] * len(clines))
        
        config_cluster_sizes = {t['id']: t['size'] for t in config_template_list}
        
        # Prepare logs tuple format
        config_logs = [(file_path, line) for line in all_config_lines]
        
        # Save config changes templates (all lines included)
        config_saved_path = save_templates_to_file(
            config_template_list, config_cluster_sizes, config_logs, config_event_ids,
            config.output_dir, "ConfigChanges", filename_prefix=stem,
            cluster_to_lines=config_cluster_to_lines,
        )
        
        saved_files['ConfigChanges'] = config_saved_path
        source_levels_list = [d['source_level'] for d in config_templates_to_merge]
        summary['ConfigChanges'] = {
            'count': len(all_config_lines),
            'templates_count': len(config_template_list),
            'path': config_saved_path,
            'source_levels': source_levels_list,
        }
        
        if config.print_progress:
            source_levels_str = ', '.join(level_names.get(l, l) for l in source_levels_list)
            print(f"  [ConfigChanges] {len(all_config_lines):,} logs -> {len(config_template_list)} templates (from {source_levels_str})")
    
    elapsed_ms = (time.perf_counter() - start_time) * 1000
    
    # Calculate stats for return value
    shifted_error_count = sum(
        sum(len(v) for v in d['cluster_to_lines'].values())
        for d in error_templates_to_merge
    ) if error_templates_to_merge else 0
    
    config_by_level = {
        level_names.get(d['source_level'], d['source_level']): sum(len(v) for v in d['cluster_to_lines'].values())
        for d in config_templates_to_merge
    } if config_templates_to_merge else {}
    
    if config.print_progress:
        print(f"\nCompleted in {elapsed_ms:.2f} ms")
        print(f"Output: {config.output_dir}")
        if shifted_error_count > 0:
            print(f"Note: {shifted_error_count} lines shifted from W/I/Unknown to Error")
        if frequency_stats:
            err_stats = frequency_stats.get('Error', {})
            if err_stats:
                hf = err_stats.get('high_freq', {})
                rare = err_stats.get('rare', {})
                print(f"Error frequency split: {hf.get('template_count', 0)} high-freq templates "
                      f"({hf.get('line_coverage_pct', 0):.1f}%), "
                      f"{rare.get('template_count', 0)} rare/priority templates "
                      f"({rare.get('line_coverage_pct', 0):.1f}%)")
    
    return {
        'file_path': file_path,
        'error': None,
        'branch_used': branch_used,
        'file_size_mb': file_size_mb,
        'elapsed_ms': elapsed_ms,
        'saved_files': saved_files,
        'summary': summary,
        'shifted_errors_count': shifted_error_count,
        'config_changes_by_level': config_by_level,
        'frequency_stats': frequency_stats,
    }


