# AGENT INTERFACE - SINGLE POINT OF CONTACT
# =============================================================================

def analyze_logs(
    file_path: str,
    output_dir: Optional[str] = None,
    config_path: Optional[str] = None,
) -> Dict:
    """
    AGENT INTERFACE: Single point of contact for log analysis.
    
    All settings are controlled via log_config.json for consistent configuration.
    
    Args:
        file_path: Path to the log file to analyze
        output_dir: Output directory for generated files (None = use config)
        config_path: Path to config JSON file (None = auto-detect)
    
    Returns:
        Production-grade JSON response:
        {
            "status": "success" | "error",
            "error": null | "error message",
            "input": {
                "file": "/path/to/input.log",
                "size_mb": 12.5
            },
            "output_dir": "/path/to/output",
            "elapsed_ms": 1234.56,
            "files": {
                "rare_errors": {"path": "...", "templates": 45, "lines": 1500},
                "highfreq_errors": {"path": "...", "templates": 5, "lines": 8500},
                "warnings": {"path": "...", "templates": 12, "lines": 340},
                "information": {"path": "...", "templates": 8, "lines": 200},
                "unknown": {"path": "...", "templates": 3, "lines": 50},
                "config_changes": {"path": "...", "templates": 7, "lines": 120}
            },
            "totals": {
                "templates": 80,
                "lines": 10710
            },
            "error_analysis": {
                "pareto_threshold": 0.80,
                "highfreq": {"templates": 5, "lines": 8500, "coverage_pct": 85.0},
                "rare": {"templates": 45, "lines": 1500, "coverage_pct": 15.0}
            }
        }
    """
    # Input validation
    if not file_path:
        return {
            'status': 'error',
            'error': 'file_path is required',
            'input': {'file': None, 'size_mb': 0},
            'output_dir': None,
            'elapsed_ms': 0,
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    file_path = str(Path(file_path).resolve())
    
    if not Path(file_path).exists():
        return {
            'status': 'error',
            'error': f'File not found: {file_path}',
            'input': {'file': file_path, 'size_mb': 0},
            'output_dir': None,
            'elapsed_ms': 0,
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    if not DRAIN3_AVAILABLE:
        return {
            'status': 'error',
            'error': 'drain3 library not installed. Install with: pip install drain3',
            'input': {'file': file_path, 'size_mb': 0},
            'output_dir': None,
            'elapsed_ms': 0,
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    # Load configuration (all flags come from config file)
    try:
        config = load_config(config_path)
    except Exception as e:
        return {
            'status': 'error',
            'error': f'Failed to load configuration: {str(e)}',
            'input': {'file': file_path, 'size_mb': 0},
            'output_dir': None,
            'elapsed_ms': 0,
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    # Apply output directory override
    if output_dir:
        config.output_dir = output_dir
    
    # Get flags from config
    flags = config.output_flags
    include_rare_errors = flags.include_rare_errors
    include_highfreq_errors = flags.include_highfreq_errors
    include_warnings = flags.include_warnings
    include_information = flags.include_information
    include_unknown = flags.include_unknown
    include_config_changes = flags.include_config_changes
    
    # Determine which levels to process based on config flags
    levels_needed = set()
    if include_rare_errors or include_highfreq_errors:
        levels_needed.add('E')
    if include_warnings:
        levels_needed.add('W')
    if include_information:
        levels_needed.add('I')
    if include_unknown:
        levels_needed.add('UNKNOWN')
    if include_config_changes:
        levels_needed.update(['W', 'I', 'UNKNOWN'])
    
    config.levels_to_process = list(levels_needed) if levels_needed else ['E', 'W', 'I', 'UNKNOWN']
    config.frequency.enable_tiered_error_output = include_rare_errors or include_highfreq_errors
    
    # Run processing with error handling
    try:
        result = process_log_file(file_path, config=config)
    except Exception as e:
        return {
            'status': 'error',
            'error': f'Processing failed: {str(e)}',
            'input': {'file': file_path, 'size_mb': 0},
            'output_dir': config.output_dir,
            'elapsed_ms': 0,
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    # Error response from process_log_file
    if result.get('error'):
        return {
            'status': 'error',
            'error': result['error'],
            'input': {'file': file_path, 'size_mb': result.get('file_size_mb', 0)},
            'output_dir': config.output_dir,
            'elapsed_ms': result.get('elapsed_ms', 0),
            'files': {},
            'totals': {'templates': 0, 'lines': 0},
            'error_analysis': None
        }
    
    # Build file outputs
    files = {}
    total_templates = 0
    total_lines = 0
    
    saved_files = result.get('saved_files', {})
    summary = result.get('summary', {})
    
    # Error outputs (tiered)
    error_data = saved_files.get('Error', {})
    error_summary = summary.get('Error', {})
    freq_class = error_summary.get('frequency_classification', {}) if isinstance(error_summary, dict) else {}
    
    if isinstance(error_data, dict):
        if include_rare_errors and 'rare' in error_data:
            t_count = freq_class.get('rare_templates', 0)
            l_count = freq_class.get('rare_lines', 0)
            files['rare_errors'] = {'path': error_data['rare'], 'templates': t_count, 'lines': l_count}
            total_templates += t_count
            total_lines += l_count
        
        if include_highfreq_errors and 'high_freq' in error_data:
            t_count = freq_class.get('high_freq_templates', 0)
            l_count = freq_class.get('high_freq_lines', 0)
            files['highfreq_errors'] = {'path': error_data['high_freq'], 'templates': t_count, 'lines': l_count}
            total_templates += t_count
            total_lines += l_count
    elif isinstance(error_data, str) and (include_rare_errors or include_highfreq_errors):
        t_count = error_summary.get('templates_count', 0)
        l_count = error_summary.get('count', 0)
        files['errors'] = {'path': error_data, 'templates': t_count, 'lines': l_count}
        total_templates += t_count
        total_lines += l_count
    
    # Warning output
    if include_warnings and 'Warning' in saved_files:
        s = summary.get('Warning', {})
        t_count, l_count = s.get('templates_count', 0), s.get('count', 0)
        files['warnings'] = {'path': saved_files['Warning'], 'templates': t_count, 'lines': l_count}
        total_templates += t_count
        total_lines += l_count
    
    # Information output
    if include_information and 'Information' in saved_files:
        s = summary.get('Information', {})
        t_count, l_count = s.get('templates_count', 0), s.get('count', 0)
        files['information'] = {'path': saved_files['Information'], 'templates': t_count, 'lines': l_count}
        total_templates += t_count
        total_lines += l_count
    
    # Unknown output
    if include_unknown and 'Unknown' in saved_files:
        s = summary.get('Unknown', {})
        t_count, l_count = s.get('templates_count', 0), s.get('count', 0)
        files['unknown'] = {'path': saved_files['Unknown'], 'templates': t_count, 'lines': l_count}
        total_templates += t_count
        total_lines += l_count
    
    # ConfigChanges output
    if include_config_changes and 'ConfigChanges' in saved_files:
        s = summary.get('ConfigChanges', {})
        t_count, l_count = s.get('templates_count', 0), s.get('count', 0)
        files['config_changes'] = {'path': saved_files['ConfigChanges'], 'templates': t_count, 'lines': l_count}
        total_templates += t_count
        total_lines += l_count
    
    # Error analysis (Pareto stats)
    error_analysis = None
    freq_stats = result.get('frequency_stats', {}).get('Error', {})
    if freq_stats:
        hf = freq_stats.get('high_freq', {})
        rare = freq_stats.get('rare', {})
        error_analysis = {
            'pareto_threshold': config.frequency.high_freq_cumulative_threshold,
            'highfreq': {
                'templates': hf.get('template_count', 0),
                'lines': freq_class.get('high_freq_lines', 0),
                'coverage_pct': round(hf.get('line_coverage_pct', 0), 1)
            },
            'rare': {
                'templates': rare.get('template_count', 0),
                'lines': freq_class.get('rare_lines', 0),
                'coverage_pct': round(rare.get('line_coverage_pct', 0), 1)
            }
        }
    
    return {
        'status': 'success',
        'error': None,
        'input': {
            'file': file_path,
            'size_mb': round(result.get('file_size_mb', 0), 2)
        },
        'output_dir': config.output_dir,
        'elapsed_ms': round(result.get('elapsed_ms', 0), 2),
        'files': files,
        'totals': {
            'templates': total_templates,
            'lines': total_lines
        },
        'error_analysis': error_analysis
    }


