def get_file_info(file_path: str) -> Dict:
    """Get file size and line count info."""
    try:
        size_bytes = os.path.getsize(file_path)
        size_mb = size_bytes / (1024 * 1024)
        
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            line_count = sum(1 for _ in f)
        
        return {
            'path': file_path,
            'name': Path(file_path).name,
            'size_mb': size_mb,
            'line_count': line_count,
        }
    except Exception as e:
        return {
            'path': file_path,
            'name': Path(file_path).name,
            'size_mb': 0,
            'line_count': 0,
            'error': str(e),
        }


def compare_branches(file_path: str, output_base: str = None) -> Dict:
    """
    Compare all three processing modes on the same file.
    
    Runs:
        1. ENTIRE_FILE branch
        2. CHUNK_SEQUENTIAL branch
        3. CHUNK_PARALLEL branch
    
    Returns comparison results with timing for each.
    """
    if output_base is None:
        output_base = str(Path(__file__).parent / "comparison_output")
    
    file_info = get_file_info(file_path)
    print(f"\n{'='*80}")
    print(f"BRANCH COMPARISON: {file_info['name']}")
    print(f"Size: {file_info['size_mb']:.2f} MB, Lines: {file_info.get('line_count', 'N/A'):,}")
    print(f"{'='*80}")
    
    results = {}
    
    # 1. ENTIRE FILE branch
    print("\n[1/3] Running ENTIRE_FILE branch...")
    results['entire_file'] = process_log_file(
        file_path,
        output_dir=f"{output_base}/entire_file",
        use_chunk_pipeline=False,
    )
    
    # 2. CHUNK SEQUENTIAL branch
    print("\n[2/3] Running CHUNK_SEQUENTIAL branch...")
    results['chunk_sequential'] = process_log_file(
        file_path,
        output_dir=f"{output_base}/chunk_sequential",
        use_chunk_pipeline=True,
        parallel_chunks=False,
    )
    
    # 3. CHUNK PARALLEL branch
    print("\n[3/3] Running CHUNK_PARALLEL branch...")
    results['chunk_parallel'] = process_log_file(
        file_path,
        output_dir=f"{output_base}/chunk_parallel",
        use_chunk_pipeline=True,
        parallel_chunks=True,
    )
    
    # Print comparison
    print(f"\n{'='*80}")
    print("TIMING COMPARISON")
    print(f"{'='*80}")
    
    for name, result in results.items():
        elapsed = result.get('elapsed_ms', 0)
        branch = result.get('branch_used', 'N/A')
        print(f"  {name:20s}: {elapsed:>10.2f} ms ({branch})")
    
    # Find fastest
    times = {k: v.get('elapsed_ms', float('inf')) for k, v in results.items()}
    fastest = min(times, key=times.get)
    print(f"\n  FASTEST: {fastest} ({times[fastest]:.2f} ms)")
    
    return {
        'file_info': file_info,
        'results': results,
        'fastest': fastest,
    }


